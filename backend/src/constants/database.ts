// DB接続プールの最大接続数。
// 同時接続が増えて足りなくなったと感じた時に増やす。
export const DB_POOL_MAX = 10;

// プールに維持する最小接続数。
// 起動直後の接続確立時間を短縮したい場合に増やす。
export const DB_POOL_MIN = 0;

// 接続取得時のタイムアウト時間（ミリ秒）。
// データベースが混雑してタイムアウトが頻発する場合に延長を検討する。
export const DB_POOL_ACQUIRE = 30000;

// アイドル状態の接続をクローズするまでの時間（ミリ秒）。
// 接続の再確立コストを抑えたい場合に長くする。
export const DB_POOL_IDLE = 10000;
